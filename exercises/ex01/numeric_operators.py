"""Prints the value of the numeric operations between two inputs."""
__author__ = "730475029"
a: int = int(input("Left Hand Side: "))
b: int = int(input("Right Hand Side: "))
print(str(a) + " ** " + str(b) + " is " + str(a**b))
print(str(a) + " / " + str(b) + " is " + str(a / b))
print(str(a) + " // " + str(b) + " is " + str(a // b))
print(str(a) + " % " + str(b) + " is " + str(a % b))
